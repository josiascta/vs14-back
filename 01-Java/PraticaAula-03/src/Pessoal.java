public class Pessoal {

    private String nome;
    private int idade;
    private int tempoDeCasa;
    private double SalarioAtual;

    public Pessoal(int idade, int tempoDeCasa, double salarioAtual, String nome) {
        this.idade = idade;
        this.tempoDeCasa = tempoDeCasa;
        SalarioAtual = salarioAtual;
        this.nome = nome;
    }
}
